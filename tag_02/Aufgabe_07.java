package tag_02;

public class Aufgabe_07 {
    public static void main(String[] args){

        int numberToMultipy = 5;
        int result;

        for (int i = 1; i<=10; i++){
            result = numberToMultipy*i;
            System.out.println(numberToMultipy + " x " + i + " = " + result );
        }
    }
    
}
